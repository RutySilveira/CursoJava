package secao13HerancaPolimorfismo.metodosAbstratos.entities.enums;

// Enum Color
// Um enum representa um conjunto FIXO de valores possíveis.
// Ele é usado quando sabemos exatamente quais opções são válidas.
public enum Color {

  // Cada item abaixo é uma constante do enum.
  // Essas constantes são:
  // - públicas (public)
  // - estáticas (static)
  // - finais (final)
  //
  // Ou seja, não podem ser alteradas e são acessadas assim:
  // Color.BLACK, Color.BLUE, Color.RED

  BLACK,
  BLUE,
  RED;

}

