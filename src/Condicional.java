// Definindo uma classe chamda Condicional
public class Condicional {
  // Método principal que é executado ao iniciar o programa
  public static void main(String[] args) {
    // Declarando e inicializando uma variavel inteira com o valor 5
    int x = 5;
    // Imprimindo "Bom dia" no console
    System.out.println("Bom dia");
    // Verificando se o valor de x é maior que 0
    if (x > 0) {
      // Se a condição for verdadeira, imprime "Boa tarde"
      System.out.println("Boa tarde");
    }
    // Imprimindo "Boa noite" no console, independete da condição
    System.out.println("Boa noite");
  }
}
