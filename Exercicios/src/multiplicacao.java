// Define uma classe chamada "multiplicacao".
public class multiplicacao {

  // Método principal que é executado quando o programa Java é iniciado.
  public static void main(String[] args) {
      
      // Declaração de uma variável inteira 'x' e 'y'.
      int x, y;
      // Atribui o valor 5 à variável 'x'.
      x = 5;
      // Atribui o dobro do valor de 'x' à variável 'y'.
      y = 2 * x;

      // Imprime o valor de 'x' no console.
      System.out.println(x);
      // Imprime o valor de 'y' no console.
      System.out.println(y);
  }
}
